    def get_rgb(val, rng = [-1, 1]):
        if val <= rng[0]:
            cmap_idx = 0
        elif val >= rng[1]:
            cmap_idx = 255
        else:
            span = rng[1] - rng[0]
            offs = span/2
            val_scaled = (val+offs)/span
            cmap_idx = math.floor(val_scaled * 255)
        rgb = ColorMap.jet[cmap_idx]
        return rgb

    
